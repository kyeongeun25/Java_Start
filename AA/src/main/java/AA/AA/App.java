package AA.AA;

/**
 * Hello world!
 *
 */
public class App 
{
    
	/**
	 * 큰 참조글, Multi comment
	 * 
	 * main은 App class내에서 아주 중요한 부분이다
	 * 
	 * @param args
	 */
	public static void main( String[] args )
    {
    	// 여기는 덧셈을 하는 곳
        // 주석 : 참조글, Remark
    	System.out.println( 123+322 );
        
        // 여기는 뺼셈을 하는 곳
        System.out.println( 123-322 );
        
        // 여기는 곱셈을 하는 곳
        System.out.println( 123*322 );
        
        // 여기는 나눗셈을 하는 곳
        System.out.println( 123/322 );
        
        // 글자들을 보이는 곳 
        System.out.println( "KOREA" );
        
        // 한글들을 보이는 곳
        System.out.println( "대한민국" );
        
        
    }
}
